public class Student {
  private String id;
  private String name;
  private double gpa;
  private int credReqs;

  public String getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public double getGpa() {
    return gpa;
  }

  public int getCredReqs() {
    return credReqs;
  }
}
